let dddInput = document.getElementById("dddId")

function DDD() {
    let ddd = dddInput.value
    
    if(ddd == 61){

        alert("Brasilia")

    }
    else if(ddd == 71){

        alert("Salvador")

    }
    else if(ddd == 11){

        alert("São Paulo")

    }
    else if(ddd == 21){

        alert("Rio de Janeiro")

    }
    else if(ddd == 32){

        alert("Juiz de Fora")

    }
    else if(ddd == 19){

        alert("Campinas")

    }
    else if(ddd == 27){

        alert("Vitoria")

    }
    else if(ddd == 31){

        alert("Belo Horizonte")
    
    }else {
        alert("DDD não cadastrado.")
    }

}